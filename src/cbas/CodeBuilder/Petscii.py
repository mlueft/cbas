    __petscii = None

    @staticmethod
    def __init():

        if Petscii.__petscii is not None:
            return
        
        Petscii.__petscii = []

        for e in Petscii.__global:
            Petscii.__petscii.append(e)
        for e in Petscii.__c64List:
            Petscii.__petscii.append(e)
        for e in Petscii.__petcat:
            Petscii.__petscii.append(e)
        for e in Petscii.__bastext:
            Petscii.__petscii.append(e)
        
        Petscii.__petscii = sorted(Petscii.__petscii, key=Petscii.cmp, reverse=True)

    @staticmethod
    def cmp(value):
        return len(value[0])
    
    @staticmethod
    def getPetscii(char):
        Petscii.__init()
        for e in Petscii.__petscii:
            if e[0] == char:
                return bytes([e[1]])
    
    @staticmethod
    def toPetscii(value):
        Petscii.__init()
        result = bytearray()

        i = 0
        while i < len(value):
            c = value[i]
            v = c
            if c == "{":
                while c != "}":
                    i+=1
                    c = value[i]
                    v += c
                
            p = Petscii.getPetscii(v)
            if p is None:
                p = bytearray()
                # v can contain characters petscii don't know
                for c in v:
                    c = Petscii.getPetscii(c)
                    if c is not None:
                        p += c

            result += p
            i += 1

        return result
    
